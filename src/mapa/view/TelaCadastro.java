    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (validarCampos()){
        cadastro cad = new cadastro();
        
        cad.setNome(campoNome.getText());
        cad.setLogin(campoLogin.getText());
        cad.setSenha(campoSenha.getText());
        cad.setEmail(campoEmail.getText());
        
        cadastroDAO cdao = new cadastroDAO();
        cdao.cadastrar(cad);
        
        limparTela();
        JOptionPane.showMessageDialog(rootPane, "Cadastro efetuado com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void limparTela(){
    campoNome.setText("");
        campoLogin.setText("");
        campoSenha.setText("");
        campoEmail.setText("");
    }
    
    private boolean validarCampos(){
    if(campoNome.getText().equals("") || campoSenha.getText().equals("") || campoEmail.getText().equals("") || campoLogin.getText().equals("")){
     JOptionPane.showMessageDialog(rootPane, "Preencha os campos", "ERRO", JOptionPane.ERROR_MESSAGE);
     return false;
    }
    return true;
    }
    
